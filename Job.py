class Job:

    def __init__(self, title, description, href, job_type, start, pay_rate,
                 location, transport):
        self.title = title
        self.description = description
        self.href = href
        self.job_type = job_type
        self.start = start
        self.pay_rate = pay_rate
        self.location = location
        self.transport = transport

